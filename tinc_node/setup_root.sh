# Completed on Thu Jul 15 00:21:02 2021
EOF


# tinc-down スクリプトの作成
# このシェルスクリプトはVPNセッションの終了時に実行される
cat > /usr/local/etc/tinc/machikado_network/tinc-down <<'EOF'
#!/bin/sh
ip link set dev $INTERFACE nomaster
ip link set dev $INTERFACE down
ip link set dev eth1 nomaster
ip link set dev br0 down
ip link del dev br0
EOF
chmod +x /usr/local/etc/tinc/machikado_network/tinc-down

# 鍵生成
tinc -n machikado_network generate-ed25519-keys | cat

#systemctl enable tinc@machikado_network.service


cat /usr/local/etc/tinc/machikado_network/hosts/"${node_name}"
